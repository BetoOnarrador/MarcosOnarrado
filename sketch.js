var barquinho
var barquinhoAnimacao
var mar
var marImagem
function preload(){
  barquinhoAnimacao=loadAnimation("ship-1.png","ship-2.png");
  marImagem=loadImage("sea.png");
}
function setup(){
  createCanvas(400 ,400);
  mar=createSprite(400,200);
  mar.addImage("GoldRoger", marImagem);
  mar.scale=0.3;
  mar.velocityX=-3;
  barquinho=createSprite(130,200,30,30);
barquinho.addAnimation("HolandesVoador",barquinhoAnimacao);
barquinho.scale=0.25;
}

function draw(){
 background("black");
  drawSprites();
  if (mar.x<0){
    mar.x=mar.width/8;
  }
}       

